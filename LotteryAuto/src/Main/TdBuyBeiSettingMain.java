    /**
     * 刷新一次手动购买界面的购买金额列表
     */
    private void REBuyMainMoneyList() {

        //获得主界面的Hash规则列表数据
        //获得正在使用的规则数据
        String use_tag = SMain.conf_rule_list.getSelectedItem().toString();
        LinkedTreeMap use_rule = (LinkedTreeMap) SMain.AllRuleInfos.get(use_tag);

        //获得当前的配置倍数
        double buy_bei = Double.parseDouble(SMain.conf_buy_double.getSelectedItem().toString());
        //找出moneylist
        ArrayList money_lists = (ArrayList) use_rule.get("money");
        //循环创建控件
        int u_x = 0;
        int u_y = 0;
        int u_h = 100;
        int u_w = 30;
        int count_b = 0;
        for (Object money_list : money_lists) {
            double one_money = Double.parseDouble(money_list.toString());
            String the_money = String.valueOf((int) (one_money * buy_bei));
            JButton newjbutton = new JButton(the_money);
            newjbutton.setPreferredSize(new Dimension(80, 30));

            newjbutton.setText(the_money + "元");
            newjbutton.setActionCommand(the_money);
            newjbutton.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(0, 0, 0), 1, true));

            //添加监听
            newjbutton.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    //点击进行资金改变
                    ChangeBuyMoney(e.getActionCommand());
                }
            });
            head_money_list.add(newjbutton);

            count_b++;
        }

        head_money_list.repaint();

    }

    /**
     * 加入对当前期当前通道的下注金额改变
     *
     * @param Money
     */
    private void ChangeBuyMoney(String Money) {
        //获得当前期与通道的组合
        String qihao = SMain.line_next_number.getText();
        if (qihao.equals("--")) {
            JOptionPane.showMessageDialog(null, "请运行程序,并且已经产生期号后在设置!");
            return;
        }
        String qihao_td = qihao + "_" + UseLabel.getToolTipText();
        //往自动里面的记录hash 加入当前期当前通道的要求的下注金额
        SMain.Auto.ChangeFiveBuyMoney.put(qihao_td, Money);
        SMain.line_next_number.setText("--");
        JOptionPane.showMessageDialog(null, "设置" + qihao_td + "将下注金额为:" + Money + ",请看界面改变!");
        this.dispose();
    }

