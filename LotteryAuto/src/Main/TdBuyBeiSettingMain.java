    /**
     * 手工改变押注单双
     *
     * @param evt
     */
    private void ChangeBuyTag(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeBuyTag
        //获得当前选中的押注tag
        String buy_tag = evt.getActionCommand();
        //开始修改掉auto的全局变量
        //获得当前期与通道的组合
        String qihao = SMain.line_next_number.getText();
        if (qihao.equals("--")) {
            JOptionPane.showMessageDialog(null, "请运行程序,并且已经产生期号后在设置!");
            return;
        }
        String qihao_td = qihao + "_" + UseLabel.getToolTipText() + "_buy";
        //往自动里面的记录hash 加入当前期当前通道的要求的下注金额
        SMain.Auto.ChangeFiveBuyMoney.put(qihao_td, buy_tag);
        SMain.line_next_number.setText("--");
        JOptionPane.showMessageDialog(null, "设置" + qihao_td + "将下注单双为:" + buy_tag + ",请看界面改变!");
        this.dispose();


    }//GEN-LAST:event_ChangeBuyTag

    /**
     * 强制下注
     *
     * @param evt
     */
    private void ChangeBuyStatus(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeBuyStatus
        //获得当前选中的押注tag
        boolean buy_status = Boolean.parseBoolean(evt.getActionCommand());
        //开始修改掉auto的全局变量
        //获得当前期与通道的组合
        String qihao = SMain.line_next_number.getText();
        if (qihao.equals("--")) {
            JOptionPane.showMessageDialog(null, "请运行程序,并且已经产生期号后在设置!");
            return;
        }
        String qihao_td = qihao + "_" + UseLabel.getToolTipText() + "_status";
        //往自动里面的记录hash 加入当前期当前通道的要求的下注金额
        if (buy_status) {
            SMain.Auto.ChangeFiveBuyMoney.put(qihao_td, buy_status);
        } else {
            SMain.Auto.ChangeFiveBuyMoney.remove(qihao_td);
        }

        SMain.line_next_number.setText("--");
        JOptionPane.showMessageDialog(null, "设置" + qihao_td + "将下注状态更改为:" + buy_status + ",请看界面改变!");
        this.dispose();
    }//GEN-LAST:event_ChangeBuyStatus

