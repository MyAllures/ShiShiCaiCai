    /**
     * 强制下注状态改变 全部
     *
     * @param evt
     */
    private void ChangeBuyStatus(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeBuyStatus
        //获得当前选中的押注tag
        boolean buy_status = Boolean.parseBoolean(evt.getActionCommand());
        //循环所有Main的当前指定通道的下注额

        Iterator iter = AllMains.entrySet().iterator();
        while (iter.hasNext()) {
            Map.Entry entry = (Map.Entry) iter.next();
            String user_name = (String) entry.getKey();
            //获得Startmian
            StartMain one_main = (StartMain) entry.getValue();

            //改变当前期的指定的通道的下注额
            String now_qihao = one_main.line_next_number.getText();
            //如果期号=--则跳过这个
            if (now_qihao.equals("--")) {
                continue;
            }
            if (buy_status) {
                one_main.Auto.ChangeFiveBuyMoney.put(now_qihao + "_" + STD + "_status", buy_status);
            } else {
                one_main.Auto.ChangeFiveBuyMoney.remove(now_qihao + "_" + STD + "_status");
            }
            //改变完毕后再清空期号,这样就会刷新
            one_main.line_next_number.setText("--");
        }
        //改变完毕,写出对应的
        UseButton.setText(STD + "改:" + buy_status);
        UseButton.setForeground(Color.yellow);
        this.dispose();
    }//GEN-LAST:event_ChangeBuyStatus

