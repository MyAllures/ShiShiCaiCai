    /**
     * 所有的金额按钮统一处理
     * <br>点击后获取金额,然后刷新所有激活Main的置顶通道的下注金额
     *
     * @param evt
     */
    private void jButtonAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAllActionPerformed
        String use_money = evt.getActionCommand(); //获得要改变的金额数
        double double_money = Double.parseDouble(use_money);
        //循环所有Main的当前指定通道的下注额

        Iterator iter = AllMains.entrySet().iterator();
        while (iter.hasNext()) {
            Map.Entry entry = (Map.Entry) iter.next();
            String user_name = (String) entry.getKey();
            //获得Startmian
            StartMain one_main = (StartMain) entry.getValue();

            // 获得下注倍数
            double buy_double = Double.parseDouble(one_main.info_buy_double.getText());

            int last_use_money = (int) (double_money * buy_double);

            //改变当前期的指定的通道的下注额
            String now_qihao = one_main.line_next_number.getText();
            //如果期号=--则跳过这个
            if (now_qihao.equals("--")) {
                continue;
            }
            one_main.Auto.ChangeFiveBuyMoney.put(now_qihao + "_" + STD, last_use_money);
            //改变完毕后再清空期号,这样就会刷新
            one_main.line_next_number.setText("--");
        }
        //改变完毕,写出对应的
        UseButton.setText(STD + "改:" + use_money);
        UseButton.setForeground(Color.yellow);
        this.dispose();
    }//GEN-LAST:event_jButtonAllActionPerformed

